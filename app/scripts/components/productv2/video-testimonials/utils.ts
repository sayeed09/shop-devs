export const getProductImageLink = (productState, productDetail) => {
    if (productState && productState.selectedImage) {
        return productState.selectedImage.src;
    } else if (productDetail && productDetail.images && productDetail.images.length > 0) {
        return productDetail.images[0].src;
    }
}